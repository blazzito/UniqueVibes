local function loadStationsList()
    local fileData = LoadResourceFile(GetCurrentResourceName(), STATIONS_LIST_FILE)
    if fileData and fileData ~= "" then
        return json.decode(fileData)
    end
    return {}
end

local function loadStationData(stationName)
    local fileData = LoadResourceFile(GetCurrentResourceName(), "config/police-stations/" .. stationName .. ".json")
    if fileData and fileData ~= "" then
        return json.decode(fileData)
    end
    return {}
end

local function GenerateLuaConfig()
    local list = loadStationsList()
    local luaCode = "Config = Config or {}\n\n-- AUTO-GENERATED CONFIGURATION\n-- Do not edit this file manually if you are using the in-game Tablet UI.\n-- The tablet UI will overwrite this file!\n\nConfig.CustomStations = {\n"
    
    for stationId, stationInfo in pairs(list) do
        luaCode = luaCode .. "    ['" .. stationId .. "'] = {\n"
        luaCode = luaCode .. "        name = '" .. (stationInfo.name or stationId) .. "',\n"
        luaCode = luaCode .. "        label = '" .. (stationInfo.label or stationId) .. "',\n"
        luaCode = luaCode .. "        active = " .. tostring(stationInfo.active) .. ",\n"
        luaCode = luaCode .. "        jobs = { police = {} },\n"
        
        local markers = loadStationData(stationId)
        luaCode = luaCode .. "        markers = {\n"
        
        for mType, mList in pairs(markers) do
            luaCode = luaCode .. "            ['" .. mType .. "'] = {\n"
            for _, m in ipairs(mList) do
                local c = m.coords
                if c then
                    luaCode = luaCode .. "                {\n"
                    if #c == 4 then
                        luaCode = luaCode .. "                    coords = vector4(" .. c[1] .. ", " .. c[2] .. ", " .. c[3] .. ", " .. c[4] .. ")\n"
                    else
                        luaCode = luaCode .. "                    coords = vector3(" .. c[1] .. ", " .. c[2] .. ", " .. c[3] .. ")\n"
                    end
                    luaCode = luaCode .. "                },\n"
                end
            end
            luaCode = luaCode .. "            },\n"
        end
        luaCode = luaCode .. "        }\n"
        luaCode = luaCode .. "    },\n"
    end
    luaCode = luaCode .. "}\n\n"
    luaCode = luaCode .. "Config.Maps = Config.Maps or {}\nlocal idx = #Config.Maps + 1\nfor k, v in pairs(Config.CustomStations) do\n    Config.Maps[idx] = v.name\n    idx = idx + 1\nend\n"
    
    SaveResourceFile(GetCurrentResourceName(), "config/custom_stations.lua", luaCode, -1)
    print("^2[DEBUG] Auto-generated config/custom_stations.lua based on JSON files.^0")
end

local function saveStationsList(data)
    SaveResourceFile(GetCurrentResourceName(), STATIONS_LIST_FILE, json.encode(data, { indent = true }), -1)
    GenerateLuaConfig()
end

local function saveStationData(stationName, data)
    SaveResourceFile(GetCurrentResourceName(), "config/police-stations/" .. stationName .. ".json", json.encode(data, { indent = true }), -1)
    GenerateLuaConfig()
end


local function isAdmin(source)
    if Config.Framework == "esx" and Framework then
        local xPlayer = Framework.GetPlayerFromId(source)
        if xPlayer then
            local group = xPlayer.getGroup()
            for _, v in pairs(Config.PermissionsGroups) do
                if v == group then
                    return true
                end
            end
            if xPlayer.job and xPlayer.job.name == Config.PoliceJobName then
                local perms = Config.Permissions[Config.PoliceJobName]
                if perms and perms.AgentManagement and xPlayer.job.grade >= perms.AgentManagement then
                    return true
                end
            end
        end
    elseif Config.Framework == "qbcore" and Framework then
        local Player = Framework.Functions.GetPlayer(source)
        if Player then
            if Framework.Functions.HasPermission(source, Config.PermissionsGroups) then
                return true
            end
        end
    end
    return false
end

