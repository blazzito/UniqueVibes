RegisterNetEvent("origen_police:admin:CreateStation")
AddEventHandler("origen_police:admin:CreateStation", function(data)
    local src = source
    if not isAdmin(src) then return end
    print("^2[DEBUG] CreateStation called with name: " .. tostring(data.name) .. " label: " .. tostring(data.label) .. "^0")
    local list = loadStationsList()
    local stationId = data.name:lower():gsub("%s+", "-")
    if not list[stationId] then
        list[stationId] = {
            name = stationId,
            label = data.label,
            active = true,
            street = "Unknown",
            author = "Admin",
            modified = os.date("%Y-%m-%d"),
            jobs = {
                police = {}
            }
        }
        saveStationsList(list)
        saveStationData(stationId, {})
        TriggerClientEvent("origen_police:client:Notify", src, "Station created successfully!", "success")
        print("^2[DEBUG] Station " .. tostring(stationId) .. " created successfully.^0")
    else
        print("^1[DEBUG] Station " .. tostring(stationId) .. " already exists.^0")
    end
end)

RegisterNetEvent("origen_police:admin:CreateMarker")
AddEventHandler("origen_police:admin:CreateMarker", function(data)
    local src = source
    local stationData = loadStationData(data.station)
    if not stationData[data.markerName] then
        stationData[data.markerName] = {}
    end
    -- Support multiple vs single
    if not data.multiple then
        stationData[data.markerName] = {}
    end
    table.insert(stationData[data.markerName], { coords = { data.coords.x, data.coords.y, data.coords.z, data.coords.h } })
    saveStationData(data.station, stationData)
end)

RegisterNetEvent("origen_police:admin:SetMarkerPos")
AddEventHandler("origen_police:admin:SetMarkerPos", function(data)
    local src = source
    local stationData = loadStationData(data.station)
    if not stationData[data.markerName] then
        stationData[data.markerName] = {}
    end
    
    if data.multiple then
        if stationData[data.markerName][data.markerIndex] then
            stationData[data.markerName][data.markerIndex].coords = { data.coords.x, data.coords.y, data.coords.z, data.coords.h }
        end
    else
        stationData[data.markerName] = {
            { coords = { data.coords.x, data.coords.y, data.coords.z, data.coords.h } }
        }
    end
    saveStationData(data.station, stationData)
end)

