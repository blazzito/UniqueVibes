local function loadRadioConfig()
    local fileData = LoadResourceFile(GetCurrentResourceName(), JSON_FILE)
    if fileData and fileData ~= "" then
        return json.decode(fileData)
    end
    return Config.CustomRadio or {}
end

local function saveRadioConfig(data)
    SaveResourceFile(GetCurrentResourceName(), JSON_FILE, json.encode(data, { indent = true }), -1)
    -- Notify all clients about the radio update
    TriggerClientEvent("origen_police:admin:radioUpdated", -1, data)
end

RegisterNetEvent("origen_police:admin:requestRadioSync")
AddEventHandler("origen_police:admin:requestRadioSync", function()
    local src = source
    local radioData = loadRadioConfig()
    TriggerClientEvent("origen_police:admin:radioUpdated", src, radioData)
end)

local function loadJobsConfig()
    local fileData = LoadResourceFile(GetCurrentResourceName(), JOBS_FILE)
